#Exercicio soma 
a = 5
b = 7
soma = a + b

print(f"A soma de {a} e {b} é {soma}")

#Exericio subtração
x = 20 
y = 8
subtracao = x - y
print(f"A subtração de {x} e {y} é {subtracao}")

#Exercicio multiplicação
resultado_multiplicação = 4 * 6
print(f"A multiplicação de 4 e 6 é {resultado_multiplicação}")

#Exercicio divisão
resultado_divisão = 15 / 3
